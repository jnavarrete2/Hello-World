#This program will takes a user input, then uses a while
#loop to check if that input is the letter A, B, or C.

usr_input = input("Give either A, B, or C: ").upper()

while usr_input != 'A' 'B' 'C':
    if usr_input == 'A':
        print("Done")
        break
    elif usr_input == 'B':
        print("Done")
        break
    elif usr_input == 'C':
        print("Done")
        break
    usr_input = input("Give either A, B, or C: ")
